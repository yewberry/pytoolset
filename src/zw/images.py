def insert2file(str, res_path, startstr, endstr):
	fp = open(res_path)
	s = fp.read()
	fp.close()
	a = s.split('\n')
	idx_s = a.index(startstr)
	idx_e = a.index(endstr)
	s = '\n'.join(a[:idx_s+1]) + '\n'+str+'\n' + '\n'.join(a[idx_e:])
	fp = open(res_path, 'w')
	fp.write(s)
	fp.close()

